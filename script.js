// ========== ПЕРЕМЕННЫЕ ==========
let clickCount = 0;

// ========== НАХОДИМ ЭЛЕМЕНТЫ ==========
const diceBtn = document.getElementById('diceBtn');
const ideaText = document.getElementById('ideaText');
const clickCountSpan = document.getElementById('clickCount');

// ========== ФУНКЦИЯ: СЛУЧАЙНАЯ ИДЕЯ ==========
function getRandomIdea() {
    const randomIndex = Math.floor(Math.random() * ideas.length);
    return ideas[randomIndex];
}

// ========== ФУНКЦИЯ: ОБНОВИТЬ ИДЕЮ ==========
function updateIdea() {
    const newIdea = getRandomIdea();
    ideaText.textContent = newIdea;
}

// ========== ФУНКЦИЯ: ВРАЩЕНИЕ КОСТИ ==========
function spinDice() {
    // Добавляем класс анимации
    diceBtn.classList.add('spinning');
    
    // Через 0.5 секунд убираем анимацию
    setTimeout(() => {
        diceBtn.classList.remove('spinning');
    }, 500);
}

// ========== ОБРАБОТЧИК КЛИКА ==========
diceBtn.addEventListener('click', function() {
    // 1. Запускаем вращение кости
    spinDice();
    
    // 2. Немного задерживаем смену идеи (чтобы кручение успело показаться)
    setTimeout(() => {
        updateIdea();
    }, 200);
    
    // 3. Увеличиваем счётчик
    clickCount++;
    clickCountSpan.textContent = clickCount;
});

// ========== ПРИВЕТСТВИЕ ПРИ ОТКРЫТИИ ==========
setTimeout(() => {
    let userName = prompt('Как тебя зовут?', 'Друг');
    if (userName && userName.trim() !== '') {
        ideaText.textContent = `Привет, ${userName}! Нажми на красную кость 🎲`;
    }
}, 300);